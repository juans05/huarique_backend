const https = require('https');
const http = require('http');
const fs = require('fs');
const path = require('path');

const BASE_URL = 'backendwarike-production.up.railway.app';
const PROTOCOL = https;

async function request(options, body = null) {
  return new Promise((resolve, reject) => {
    const req = PROTOCOL.request(options, (res) => {
      let data = '';
      res.on('data', (chunk) => data += chunk);
      res.on('end', () => {
        try {
          const json = data ? JSON.parse(data) : {};
          resolve({ status: res.statusCode, data: json });
        } catch (e) {
          resolve({ status: res.statusCode, data: data });
        }
      });
    });
    req.on('error', reject);
    if (body) {
      if (typeof body === 'string') {
        req.write(body);
      } else {
        req.write(body);
      }
    }
    req.end();
  });
}

// Custom multipart/form-data for image upload without external deps
async function uploadImage(token, filePath) {
  const boundary = '----WebKitFormBoundary' + Math.random().toString(36).substring(2);
  const filename = path.basename(filePath);
  const fileContent = fs.readFileSync(filePath);

  let body = Buffer.concat([
    Buffer.from(`--${boundary}\r\n`),
    Buffer.from(`Content-Disposition: form-data; name="file"; filename="${filename}"\r\n`),
    Buffer.from(`Content-Type: image/png\r\n\r\n`),
    fileContent,
    Buffer.from(`\r\n--${boundary}--\r\n`),
  ]);

  const options = {
    hostname: BASE_URL,
    path: '/upload/image',
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${token}`,
      'Content-Type': `multipart/form-data; boundary=${boundary}`,
      'Content-Length': body.length,
    }
  };

  return request(options, body);
}

async function runTest() {
  console.log('🚀 Starting Full Flow Test on Railway...');

  // 1. Login
  console.log('🔐 Step 1: Logging in...');
  const loginBody = JSON.stringify({
    email: 'admin@wuarike.com',
    password: '123456'
  });
  const loginRes = await request({
    hostname: BASE_URL,
    path: '/auth/login',
    method: 'POST',
    headers: { 'Content-Type': 'application/json', 'Content-Length': loginBody.length }
  }, loginBody);

  if (loginRes.status !== 200) {
    console.error('❌ Login failed:', loginRes.data);
    return;
  }
  const token = loginRes.data.accessToken;
  console.log('✅ Logged in successfully.');

  // 2. Get Categories
  console.log('📂 Step 2: Fetching categories...');
  const catRes = await request({
    hostname: BASE_URL,
    path: '/places/categories',
    method: 'GET',
    headers: { 'Authorization': `Bearer ${token}` }
  });
  
  if (catRes.status !== 200 || !catRes.data.length) {
    console.error('❌ Failed to get categories:', catRes.data);
    return;
  }
  const categoryId = catRes.data[0].id;
  console.log(`✅ Found category: ${catRes.data[0].name} (${categoryId})`);

  // 3. Upload Image
  console.log('📸 Step 3: Uploading test image...');
  // Create a dummy 1x1 white pixel PNG or just use any small file
  const dummyFilePath = path.join(__dirname, 'test_image.png');
  const dummyPngBase64 = 'iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mP8/5+hHgAHggJ/PchI7wAAAABJRU5ErkJggg==';
  fs.writeFileSync(dummyFilePath, Buffer.from(dummyPngBase64, 'base64'));

  const uploadRes = await uploadImage(token, dummyFilePath);
  if (uploadRes.status !== 201) {
    console.error('❌ Image upload failed:', uploadRes.data);
    return;
  }
  const imageUrl = uploadRes.data.url;
  console.log('✅ Image uploaded:', imageUrl);

  // 4. Submit Place
  console.log('📝 Step 4: Submitting new place...');
  const submissionBody = JSON.stringify({
    name: 'Test Warike ' + Date.now(),
    description: 'A test place generated by the system',
    categoryId: categoryId,
    district: 'San Miguel',
    address: 'Av. Universitaria 123',
    latitude: -12.0833,
    longitude: -77.0833,
    coverImageUrl: imageUrl
  });
  
  const submitRes = await request({
    hostname: BASE_URL,
    path: '/places/submissions',
    method: 'POST',
    headers: { 
      'Authorization': `Bearer ${token}`,
      'Content-Type': 'application/json',
      'Content-Length': submissionBody.length 
    }
  }, submissionBody);

  if (submitRes.status !== 201) {
    console.error('❌ Submission failed:', submitRes.data);
    return;
  }
  const submissionId = submitRes.data.id;
  console.log('✅ Place submitted successfully. ID:', submissionId);

  // 5. Verify in Admin
  console.log('🛡️ Step 5: Verifying in Admin Panel...');
  const adminRes = await request({
    hostname: BASE_URL,
    path: '/admin/submissions',
    method: 'GET',
    headers: { 'Authorization': `Bearer ${token}` }
  });

  if (adminRes.status !== 200) {
    console.error('❌ Failed to fetch admin list:', adminRes.data);
    return;
  }

  const found = adminRes.data.find(s => s.id === submissionId);
  if (found) {
    console.log('🎉 SUCCESS! The submission is visible in the pending list.');
  } else {
    console.warn('⚠️ Submission not found in the list. Count:', adminRes.data.length);
  }

  // Cleanup
  fs.unlinkSync(dummyFilePath);
}

runTest().catch(console.error);
